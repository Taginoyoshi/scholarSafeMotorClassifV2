def load_and_reshape_data(filepath: str, verbose: bool = True) -> Tuple[pd.DataFrame, pd.Series, List[str]]:
    """Apenas carrega, pivota e separa features do target. NENHUMA estatística é calculada aqui."""
    if verbose: print(f"[1/12] Carregando e estruturando dataset bruto: {filepath}")
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"[ERRO] Arquivo '{filepath}' não encontrado.")

    df = pd.read_csv(filepath)

    if 'Desc_Sit_Matricula' not in df.columns:
        raise ValueError("Coluna 'Desc_Sit_Matricula' não encontrada.")

    status_map = {
        'Formado': 'CONCLUÍDO',
        'Abandono': 'CANCELADO',
        'Cancelado Voluntariamente': 'CANCELADO',
        'Cancelado Compulsoriamente': 'CANCELADO'
    }
    df['status'] = df['Desc_Sit_Matricula'].map(status_map)
    df = df.dropna(subset=['status'])

    subject_cols = ['DESC_DISCIPLINA', 'CARGA_HOR', 'Sit_Disciplina', 'N_FALTAS', 'NOTA', 'Equivalencia']
    student_cols = [c for c in df.columns if c not in subject_cols]

    df_student = df[student_cols].drop_duplicates(subset=['Cod_Pessoa'])
    pivot_df = df.pivot_table(index='Cod_Pessoa', columns='DESC_DISCIPLINA', values='NOTA', aggfunc='max')
    df_merged = df_student.merge(pivot_df, on='Cod_Pessoa', how='inner')

    cols_to_drop_init = ['Cod_Pessoa', 'Desc_Sit_Matricula']
    df_merged = df_merged.drop(columns=[c for c in cols_to_drop_init if c in df_merged.columns])

    X = df_merged.drop(columns=['status'])
    y = df_merged['status']
    subject_names = pivot_df.columns.tolist()

    return X, y, subject_names

def handle_data_leakage(X_train: pd.DataFrame, y_train: np.ndarray, threshold: float = 0.30, verbose: bool = True) -> List[str]:
    """Calcula a Informação Mútua (MI) ESTRITAMENTE nos dados de treino para evitar Leakage."""
    if verbose: print(f"[2/12] Calculando Informação Mútua (MI) apenas no Treino para detectar vazamentos...")

    X_mi = X_train.copy()
    # Preenchimento temporário apenas para o cálculo estatístico do MI
    for col in X_mi.columns:
        if X_mi[col].dtype == 'object':
            X_mi[col] = LabelEncoder().fit_transform(X_mi[col].astype(str))
        else:
            X_mi[col] = X_mi[col].fillna(-1)

    mi_scores = mutual_info_classif(X_mi, y_train, random_state=42)
    mi_series = pd.Series(mi_scores, index=X_mi.columns).sort_values(ascending=False)

    leakage_cols = mi_series[mi_series > threshold].index.tolist()

    if verbose and leakage_cols:
        print(f"      > ATENÇÃO: {len(leakage_cols)} colunas identificadas como vazamento (MI > {threshold}).")
        for col, score in mi_series.head(5).items():
            if score > threshold: print(f"        - {col}: {score:.4f}")

    return leakage_cols

def build_and_fit_pipeline(X_train: pd.DataFrame, verbose: bool = True) -> Tuple[ColumnTransformer, List[str]]:
    """Constrói e ajusta o pipeline de preprocessamento ESTRITAMENTE nos dados de treino."""
    if verbose: print("[4/12] Ajustando Scikit-Learn Pipeline (Imputer e Scaler) apenas no Treino...")

    cat_cols = X_train.select_dtypes(include=['object', 'category']).columns
    num_cols = X_train.select_dtypes(include=['number']).columns

    # As medianas numéricas serão aprendidas APENAS no X_train
    num_pipeline = Pipeline([
        ('imputer', SimpleImputer(strategy='median')),
        ('scaler', MaxAbsScaler())
    ])

    cat_pipeline = Pipeline([
        ('imputer', SimpleImputer(strategy='constant', fill_value='Não Informado')),
        ('encoder', OneHotEncoder(sparse_output=True, handle_unknown='ignore'))
    ])

    preprocessor = ColumnTransformer(
        transformers=[
            ('num', num_pipeline, num_cols),
            ('cat', cat_pipeline, cat_cols)
        ],
        remainder='drop'
    )

    preprocessor.fit(X_train)
    expected_features = X_train.columns.tolist()
    return preprocessor, expected_features

