def predict_new_students(new_filepath: str, artifacts_filepath: str = 'modelo_evasao_artifacts.pkl'):
    artifacts = load_model_artifacts(artifacts_filepath)
    model, preprocessor, expected_features = artifacts['model'], artifacts['preprocessor'], artifacts['expected_features']
    subject_names, leakage_cols = artifacts['subject_names'], artifacts['leakage_cols']

    print(f"\n[PRODUÇÃO] Processando arquivo de novos alunos: {new_filepath}")
    df = pd.read_csv(new_filepath)
    subject_cols = ['DESC_DISCIPLINA', 'CARGA_HOR', 'Sit_Disciplina', 'N_FALTAS', 'NOTA', 'Equivalencia']
    student_cols = [c for c in df.columns if c not in subject_cols]
    df_student = df[student_cols].drop_duplicates(subset=['Cod_Pessoa'])

    if 'DESC_DISCIPLINA' in df.columns and 'NOTA' in df.columns:
        pivot_df = df.pivot_table(index='Cod_Pessoa', columns='DESC_DISCIPLINA', values='NOTA', aggfunc='max')
        df_merged = df_student.merge(pivot_df, on='Cod_Pessoa', how='left')
    else:
        df_merged = df_student.copy()

    # Aplica os expurgos descobertos no treinamento
    df_merged = df_merged.drop(columns=[c for c in leakage_cols if c in df_merged.columns])

    for col in expected_features:
        if col not in df_merged.columns:
            # Disciplina não cursada recebe 0. Demais features faltantes receberão np.nan e serão tratadas pela Mediana do Treino.
            df_merged[col] = 0.0 if col in subject_names else np.nan

    X_new = df_merged[expected_features]
    X_new_processed = preprocessor.transform(X_new)

    probabilidades = model.predict_proba(X_new_processed)[:, 1]
    resultados = pd.DataFrame({'Cod_Pessoa': df_merged['Cod_Pessoa'], 'Chance_Evasao_Perc': probabilidades * 100})
    resultados['Risco'] = np.where(resultados['Chance_Evasao_Perc'] > 50.0, 'ALTO RISCO (Evasão)', 'BAIXO RISCO (Conclusão)')

    print("\n      > Predições Finalizadas! Amostra do Sistema de IA:")
    print(resultados.head(10).to_string(index=False))
    return resultados

