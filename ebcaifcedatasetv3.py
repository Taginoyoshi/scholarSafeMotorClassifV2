def optimize_hyperparams(params, X_tr, y_tr, X_v, y_v):
    scores = []
    for param in params:
        t_val = max(1, int(round(param[0])))
        lgbm_leaves = max(31, int(round(param[1])))
        xgb_depth = max(3, int(round(param[2])))

        model = EBCADeepCascadeForest(max_layers=2, k_fold=3, T=t_val, lgbm_num_leaves=lgbm_leaves, xgb_max_depth=xgb_depth, random_state=42)
        model.fit(X_tr, y_tr, verbose=False)
        preds = model.predict_proba(X_v)[:, 1]
        auc = roc_auc_score(y_v, preds)
        scores.append(1.0 - auc)
    return np.array(scores)

def save_model_artifacts(model, preprocessor, le, expected_features, subject_names, leakage_cols, filepath='modelo_evasao_artifacts.pkl'):
    print(f"\n[EXPORT] Salvando modelo e artefatos de pré-processamento em: {filepath}")
    artifacts = {
        'model': model,
        'preprocessor': preprocessor,
        'label_encoder': le,
        'expected_features': expected_features,
        'subject_names': subject_names,
        'leakage_cols': leakage_cols # Importante salvar para limpar dados novos
    }
    joblib.dump(artifacts, filepath)
    print("      > Exportação concluída com sucesso!")

def load_model_artifacts(filepath='modelo_evasao_artifacts.pkl') -> dict:
    return joblib.load(filepath)

